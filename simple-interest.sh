#!/bin/bash

# Этот скрипт вычисляет простые проценты с учетом основной суммы,
# годовой процентной ставки и период времени в годах.

# Используйте это только в качестве образца.

# Авторы: 
# <ваше имя пользователя GitHub>.

# Вход:
# p, основная сумма
# t, период времени в годах
# r, годовая процентная ставка

# Выход:
# простые проценты = p*t*r

echo "Введите имя директора:"
read p
echo "Введите процентную ставку в год:"
read р
echo "Введите период времени в годах:"
read т

s=`expr $p \* $t \* $r / 100`
echo "Простые проценты составляют: "
echo $s 
